    public function editarRegistro(Request $request)
    {
        $user = User::findOrFail($request->id);
        $user->name = $request->name;
        $user->email = $request->email;
        if ($request->image_id <> '') {
            $user->image_id = $request->image_id;
        }
        $user->save();
        return response()->json($user);
    }
